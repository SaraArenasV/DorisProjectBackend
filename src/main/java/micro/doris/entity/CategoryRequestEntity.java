package micro.doris.entity;

public class CategoryRequestEntity {

	private Integer id;
	private String name;

	public CategoryRequestEntity() {
		super();
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
